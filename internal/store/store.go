// Package store is the brain's SQLite persistence — the desired-state source
// of truth (CONTROL_PLANE.md, APP_LIFECYCLE.md). Skeleton scope: the app
// instances table only. Users, sessions, audit, etc. land later.
package store

import (
	"database/sql"
	"errors"
	"fmt"
	"strings"
	"time"

	_ "modernc.org/sqlite"
)

// Instance is one installed app (a compose project, APP_LIFECYCLE.md).
//
// Every instance is owner-scoped (DASHBOARD.md # the apps model). A household
// instance is admin-owned on behalf of everyone and takes the bare slug; a
// personal instance is owned by one user and takes the `<slug>--<user>` slug.
// OwnerUserID is a plain reference (no FK): user deletion vs. owned instances
// is unspecced (NEXT.md), so we don't couple deleteUser to ownership yet.
type Instance struct {
	ID          string
	ManifestID  string
	Name        string
	Slug        string
	Version     string
	State       string // installing | running | stopped | failed | uninstalling
	MDNSName    string
	OwnerUserID string
	Scope       string // ScopeHousehold | ScopePersonal
	// ServiceUID/GID is the dedicated app-service identity allocated for a
	// folderless `service_user: true` instance (APP_ISOLATION.md # Runtime
	// identity & data ownership). 0 = none — allocated values are always in
	// the reserved band (≥ protocol.AppServiceUIDMin), never root. Persisted
	// so the identity is stable across container recreations and releasable
	// at uninstall.
	ServiceUID int
	ServiceGID int
	CreatedAt  time.Time
}

const (
	ScopeHousehold = "household"
	ScopePersonal  = "personal"
)

// User is a malmo dashboard account. The brain mirrors a Linux account
// (USERS_AND_GROUPS.md); the *password* lives in PAM via host-agent, never
// in this row. `recovery_hash` is set only for admins (AUTH.md # Recovery).
type User struct {
	ID           string
	Username     string
	Role         string // "admin" | "member"
	RecoveryHash string // argon2id of recovery code; empty for non-admin
	CreatedAt    time.Time
}

const (
	RoleAdmin  = "admin"
	RoleMember = "member"
)

// Session is an issued dashboard session. Tokens are opaque random bytes,
// generated by the auth package and used as the cookie value (AUTH.md #
// Sessions). `last_seen_at` is updated on each authenticated request so the
// session can age out independently of issue time.
type Session struct {
	Token         string
	UserID        string
	CreatedAt     time.Time
	LastSeenAt    time.Time
	ExpiresAt     time.Time // absolute hard cap (90 days from issue)
	ElevatedUntil time.Time // zero value means not elevated
}

// ErrConflict signals a uniqueness collision (duplicate username, second
// admin during /v1/setup). The API maps it to 409.
var ErrConflict = errors.New("conflict")

// ErrNotFound covers any missing row — instance, user, or session. The API
// maps it to 404 (or 401 for sessions; the auth middleware translates).
var ErrNotFound = errors.New("not found")

type Store struct{ db *sql.DB }

func Open(path string) (*Store, error) {
	db, err := sql.Open("sqlite", path)
	if err != nil {
		return nil, err
	}
	db.SetMaxOpenConns(1) // sqlite: serialize writers, avoid "database is locked"
	if _, err := db.Exec(`PRAGMA journal_mode=WAL; PRAGMA foreign_keys=ON;`); err != nil {
		return nil, err
	}
	s := &Store{db: db}
	if err := s.migrate(); err != nil {
		return nil, err
	}
	return s, nil
}

func (s *Store) Close() error { return s.db.Close() }

func (s *Store) migrate() error {
	_, err := s.db.Exec(`
		CREATE TABLE IF NOT EXISTS instances (
			id          TEXT PRIMARY KEY,
			manifest_id TEXT NOT NULL,
			name        TEXT NOT NULL,
			slug        TEXT NOT NULL UNIQUE,
			version     TEXT NOT NULL,
			state       TEXT NOT NULL,
			mdns_name   TEXT NOT NULL DEFAULT '',
			owner_user_id TEXT NOT NULL DEFAULT '',
			scope         TEXT NOT NULL DEFAULT 'household' CHECK (scope IN ('household','personal')),
			service_uid INTEGER NOT NULL DEFAULT 0,
			service_gid INTEGER NOT NULL DEFAULT 0,
			created_at  INTEGER NOT NULL
		);
		CREATE TABLE IF NOT EXISTS instance_images (
			instance_id TEXT NOT NULL,
			service     TEXT NOT NULL,
			image       TEXT NOT NULL,
			digest      TEXT NOT NULL,
			PRIMARY KEY (instance_id, service),
			FOREIGN KEY (instance_id) REFERENCES instances(id) ON DELETE CASCADE
		);
		CREATE TABLE IF NOT EXISTS instance_secrets (
			instance_id TEXT NOT NULL,
			name        TEXT NOT NULL,
			value       TEXT NOT NULL,
			PRIMARY KEY (instance_id, name),
			FOREIGN KEY (instance_id) REFERENCES instances(id) ON DELETE CASCADE
		);
		-- service_instances: one shared managed-service container per type+version
		-- (SERVICE_PROVISIONING.md # Tier 1). The brain spins these up lazily and
		-- owns the superuser credential it provisions per-app databases with. Not
		-- an app instance: no slug, owner, route, or mDNS. superuser_password is
		-- plaintext at rest (same trust model as instance_secrets; hardening
		-- deferred, NEXT.md # App-secret injection hardening).
		CREATE TABLE IF NOT EXISTS service_instances (
			kind               TEXT    NOT NULL,
			version            TEXT    NOT NULL,
			superuser_password TEXT    NOT NULL,
			state              TEXT    NOT NULL,
			created_at         INTEGER NOT NULL,
			PRIMARY KEY (kind, version)
		);
		-- service_grants: the per-app database+role the brain provisioned inside a
		-- service_instance (SERVICE_PROVISIONING.md # Provisioning protocol). One
		-- row per (app instance, logical service name); injected back as
		-- MALMO_SERVICE_<NAME>_*. Cascades with the app instance so uninstall
		-- reclaims the bookkeeping (the DB/role drop is a separate docker-exec
		-- step in lifecycle). password is plaintext at rest, as above.
		CREATE TABLE IF NOT EXISTS service_grants (
			instance_id  TEXT NOT NULL,
			logical_name TEXT NOT NULL,
			kind         TEXT NOT NULL,
			version      TEXT NOT NULL,
			db_name      TEXT NOT NULL,
			role_name    TEXT NOT NULL,
			password     TEXT NOT NULL,
			PRIMARY KEY (instance_id, logical_name),
			FOREIGN KEY (instance_id) REFERENCES instances(id) ON DELETE CASCADE
		);
		-- mail_providers: admin-registered outgoing SMTP providers (BYO outgoing
		-- mail, SERVICE_PROVISIONING.md). The brain holds the credential and
		-- injects it into bound apps as MALMO_MAIL_*; there is no malmo-run
		-- relay. password is plaintext at rest (same trust model as
		-- instance_secrets; hardening deferred, NEXT.md # App-secret injection
		-- hardening).
		CREATE TABLE IF NOT EXISTS mail_providers (
			id           TEXT    PRIMARY KEY,
			label        TEXT    NOT NULL UNIQUE,
			host         TEXT    NOT NULL,
			port         INTEGER NOT NULL,
			username     TEXT    NOT NULL,
			password     TEXT    NOT NULL,
			from_address TEXT    NOT NULL,
			encryption   TEXT    NOT NULL CHECK (encryption IN ('none','starttls','tls')),
			created_at   INTEGER NOT NULL
		);
		-- instance_mail_bindings: which provider a mail-capable app sends
		-- through — at most one per instance; an unbound instance gets no
		-- MALMO_MAIL_* vars at all. Cascades with the app instance, and with
		-- the provider so deleting a provider unbinds its apps (never the
		-- reverse — apps always survive provider deletion).
		CREATE TABLE IF NOT EXISTS instance_mail_bindings (
			instance_id TEXT PRIMARY KEY REFERENCES instances(id) ON DELETE CASCADE,
			provider_id TEXT NOT NULL REFERENCES mail_providers(id) ON DELETE CASCADE
		);
		CREATE TABLE IF NOT EXISTS users (
			id            TEXT PRIMARY KEY,
			username      TEXT NOT NULL UNIQUE,
			role          TEXT NOT NULL CHECK (role IN ('admin','member')),
			recovery_hash TEXT NOT NULL DEFAULT '',
			created_at    INTEGER NOT NULL
		);
		CREATE TABLE IF NOT EXISTS sessions (
			token          TEXT PRIMARY KEY,
			user_id        TEXT NOT NULL,
			created_at     INTEGER NOT NULL,
			last_seen_at   INTEGER NOT NULL,
			expires_at     INTEGER NOT NULL DEFAULT 0,
			elevated_until INTEGER NOT NULL DEFAULT 0,
			FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
		);
		CREATE TABLE IF NOT EXISTS audit_events (
			id            INTEGER PRIMARY KEY AUTOINCREMENT,
			ts            INTEGER NOT NULL,
			actor_user_id TEXT    NULL REFERENCES users(id) ON DELETE SET NULL,
			actor_role    TEXT    NOT NULL,
			action        TEXT    NOT NULL,
			target_kind   TEXT    NULL,
			target_id     TEXT    NULL,
			source_ip     TEXT    NULL,
			success       INTEGER NOT NULL,
			metadata      TEXT    NULL
		);
		CREATE TRIGGER IF NOT EXISTS audit_events_no_update
			BEFORE UPDATE ON audit_events
			WHEN NOT (NEW.actor_user_id IS NULL AND OLD.actor_user_id IS NOT NULL
			          AND NEW.ts = OLD.ts AND NEW.actor_role = OLD.actor_role
			          AND NEW.action = OLD.action AND NEW.success = OLD.success)
			BEGIN SELECT RAISE(ABORT, 'audit_events is append-only'); END;
		CREATE TRIGGER IF NOT EXISTS audit_events_no_delete
			BEFORE DELETE ON audit_events
			BEGIN SELECT RAISE(ABORT, 'audit_events is append-only'); END;
		CREATE TABLE IF NOT EXISTS health_issues (
			id              TEXT    NOT NULL,
			instance_key    TEXT    NOT NULL DEFAULT '',
			category        TEXT    NOT NULL,
			severity        TEXT    NOT NULL,
			tier            INTEGER NOT NULL,
			blocks_writes   INTEGER NOT NULL,
			blocks_apps     INTEGER NOT NULL,
			blocks_users    INTEGER NOT NULL,
			summary         TEXT    NOT NULL,
			details         TEXT    NOT NULL DEFAULT '',
			raised_at       INTEGER NOT NULL,
			last_checked_at INTEGER NOT NULL,
			PRIMARY KEY (id, instance_key)
		);
		-- notifications: the dashboard notification center (NOTIFICATIONS.md).
		-- Mutable and prunable, unlike audit_events. The full spec shape is
		-- created up front so later slices (read-state, dismiss, the API) add
		-- behavior without a migration; read_at / dismissed_at stay NULL until
		-- those land. The partial unique index enforces the coalescing
		-- invariant: at most one *active* (non-dismissed) notification per
		-- dedup_key.
		CREATE TABLE IF NOT EXISTS notifications (
			id            INTEGER PRIMARY KEY AUTOINCREMENT,
			ts            INTEGER NOT NULL,
			category      TEXT    NOT NULL,
			severity      TEXT    NOT NULL,
			source_kind   TEXT    NOT NULL,
			source_id     TEXT    NOT NULL,
			dedup_key     TEXT    NOT NULL,
			audience      TEXT    NOT NULL,
			user_id       TEXT    NULL REFERENCES users(id) ON DELETE CASCADE,
			variant       TEXT    NOT NULL,
			summary       TEXT    NOT NULL,
			body          TEXT    NOT NULL DEFAULT '',
			action_label  TEXT    NOT NULL DEFAULT '',
			action_route  TEXT    NOT NULL DEFAULT '',
			read_at       INTEGER NULL,
			dismissed_at  INTEGER NULL,
			resolved_at   INTEGER NULL
		);
		CREATE UNIQUE INDEX IF NOT EXISTS notifications_active_dedup
			ON notifications(dedup_key) WHERE dismissed_at IS NULL;
		-- notification_reads: per-recipient read/dismiss state (NOTIFICATIONS.md
		-- # Read / unread / dismiss). A box-wide ('admins') notification is one
		-- row in notifications but read/unread *per admin*, so read state can't
		-- live on the notification row — it lives here, one row per (notification,
		-- user). Used uniformly for every audience (the row-level read_at/
		-- dismissed_at columns on notifications stay reserved). ON DELETE CASCADE
		-- on both FKs so pruning a notification or deleting a user cleans up.
		CREATE TABLE IF NOT EXISTS notification_reads (
			notification_id INTEGER NOT NULL REFERENCES notifications(id) ON DELETE CASCADE,
			user_id         TEXT    NOT NULL REFERENCES users(id)         ON DELETE CASCADE,
			read_at         INTEGER NULL,
			dismissed_at    INTEGER NULL,
			PRIMARY KEY (notification_id, user_id)
		);
		-- notification_mutes: per-user, per-category mute (NOTIFICATIONS.md
		-- # Configuration). Presence of a row means the category is muted for that
		-- user; absence means on — "everything on by default", so a new user has no
		-- rows and sees everything. The (user_id, category) PK fully models the
		-- mute; unmute is a DELETE. Mute is a read-time filter (the list/count/
		-- mark-all queries exclude muted categories), never emit-time suppression:
		-- a box-wide 'admins'/'members' notification is one row shared by many
		-- recipients, so it can't be withheld per-user at write time. ON DELETE
		-- CASCADE cleans up when the user is deleted.
		CREATE TABLE IF NOT EXISTS notification_mutes (
			user_id   TEXT NOT NULL REFERENCES users(id) ON DELETE CASCADE,
			category  TEXT NOT NULL,
			PRIMARY KEY (user_id, category)
		);
		-- box_meta: a tiny key/value table for singleton box-level facts that
		-- aren't per-instance and don't warrant their own table. Today it holds
		-- the hosted-profile provisioning identity (ENVIRONMENT.md # Provisioning):
		-- the box-id and the hash of the one-time admin-bootstrap secret. The
		-- appliance profile never writes a row, so the table stays empty there.
		CREATE TABLE IF NOT EXISTS box_meta (
			key   TEXT PRIMARY KEY,
			value TEXT NOT NULL
		);`)
	if err != nil {
		return err
	}

	// Idempotent migrations: add new columns when an older DB predates them.
	// SQLite doesn't support IF NOT EXISTS on ALTER TABLE; we detect existence
	// by probing PRAGMA table_info and skip if the column is already there.
	for _, col := range []struct {
		table string
		name  string
		stmt  string
	}{
		{"sessions", "expires_at", "ALTER TABLE sessions ADD COLUMN expires_at INTEGER NOT NULL DEFAULT 0"},
		{"sessions", "elevated_until", "ALTER TABLE sessions ADD COLUMN elevated_until INTEGER NOT NULL DEFAULT 0"},
		{"instances", "owner_user_id", "ALTER TABLE instances ADD COLUMN owner_user_id TEXT NOT NULL DEFAULT ''"},
		{"instances", "scope", "ALTER TABLE instances ADD COLUMN scope TEXT NOT NULL DEFAULT 'household'"},
		{"instances", "service_uid", "ALTER TABLE instances ADD COLUMN service_uid INTEGER NOT NULL DEFAULT 0"},
		{"instances", "service_gid", "ALTER TABLE instances ADD COLUMN service_gid INTEGER NOT NULL DEFAULT 0"},
	} {
		has, hErr := s.hasColumn(col.table, col.name)
		if hErr != nil {
			return hErr
		}
		if !has {
			if _, err := s.db.Exec(col.stmt); err != nil {
				return err
			}
		}
	}

	// Backfill expires_at = created_at + 90 days for rows that have the default 0.
	const ninetyDays = 90 * 24 * 60 * 60 // seconds
	if _, err := s.db.Exec(
		`UPDATE sessions SET expires_at = created_at + ? WHERE expires_at = 0`, ninetyDays,
	); err != nil {
		return err
	}

	// Backfill owner_user_id for instances installed before owner-scoping: assign
	// the bootstrap admin (oldest admin) so legacy rows have a valid owner. New
	// rows always carry an explicit owner from the install transaction.
	if _, err := s.db.Exec(
		`UPDATE instances SET owner_user_id = (SELECT id FROM users WHERE role='admin' ORDER BY created_at LIMIT 1)
		 WHERE owner_user_id = '' AND EXISTS (SELECT 1 FROM users WHERE role='admin')`,
	); err != nil {
		return err
	}

	return nil
}

// hasColumn reports whether table has a column of the given name.
func (s *Store) hasColumn(table, name string) (bool, error) {
	rows, err := s.db.Query(`PRAGMA table_info(` + table + `)`)
	if err != nil {
		return false, err
	}
	defer rows.Close()
	for rows.Next() {
		var cid, notnull, pk int
		var cname, ctype string
		var dflt any
		if err := rows.Scan(&cid, &cname, &ctype, &notnull, &dflt, &pk); err != nil {
			return false, err
		}
		if cname == name {
			return true, nil
		}
	}
	return false, rows.Err()
}

// InstanceImage is the resolved image pin for one service in one instance
// (APP_LIFECYCLE.md # image digest pinning).
type InstanceImage struct {
	Service string
	Image   string // original `image:tag` reference from the author's compose
	Digest  string // `sha256:…`
}

// SetInstanceImages replaces the pinned images for an instance in one
// transaction. Called once per install (and later, per update) after digests
// have been resolved.
func (s *Store) SetInstanceImages(instanceID string, images []InstanceImage) error {
	tx, err := s.db.Begin()
	if err != nil {
		return err
	}
	defer tx.Rollback()
	if _, err := tx.Exec(`DELETE FROM instance_images WHERE instance_id=?`, instanceID); err != nil {
		return err
	}
	for _, img := range images {
		if _, err := tx.Exec(
			`INSERT INTO instance_images (instance_id, service, image, digest) VALUES (?,?,?,?)`,
			instanceID, img.Service, img.Image, img.Digest); err != nil {
			return err
		}
	}
	return tx.Commit()
}

// GetInstanceImages returns the pinned images for an instance, ordered by
// service name.
func (s *Store) GetInstanceImages(instanceID string) ([]InstanceImage, error) {
	rows, err := s.db.Query(
		`SELECT service, image, digest FROM instance_images
		 WHERE instance_id=? ORDER BY service`, instanceID)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	var out []InstanceImage
	for rows.Next() {
		var i InstanceImage
		if err := rows.Scan(&i.Service, &i.Image, &i.Digest); err != nil {
			return nil, err
		}
		out = append(out, i)
	}
	return out, rows.Err()
}

// InstanceSecret is one generated per-app secret (SERVICE_PROVISIONING.md #
// Env-var injection). Name is the manifest-declared snake_case key; Value is
// the generated, encoded secret the brain re-emits as MALMO_SECRET_<NAME>.
type InstanceSecret struct {
	Name  string
	Value string
}

// SetInstanceSecrets persists an instance's generated secrets in one
// transaction. Called once per install, after the rows are generated. The
// values are written verbatim (plaintext at rest — the .env on disk holds the
// same value; row-level encryption is deferred, NEXT.md # App-secret injection
// hardening).
func (s *Store) SetInstanceSecrets(instanceID string, secrets []InstanceSecret) error {
	tx, err := s.db.Begin()
	if err != nil {
		return err
	}
	defer tx.Rollback()
	if _, err := tx.Exec(`DELETE FROM instance_secrets WHERE instance_id=?`, instanceID); err != nil {
		return err
	}
	for _, sec := range secrets {
		if _, err := tx.Exec(
			`INSERT INTO instance_secrets (instance_id, name, value) VALUES (?,?,?)`,
			instanceID, sec.Name, sec.Value); err != nil {
			return err
		}
	}
	return tx.Commit()
}

// GetInstanceSecrets returns an instance's generated secrets, ordered by name.
func (s *Store) GetInstanceSecrets(instanceID string) ([]InstanceSecret, error) {
	rows, err := s.db.Query(
		`SELECT name, value FROM instance_secrets
		 WHERE instance_id=? ORDER BY name`, instanceID)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	var out []InstanceSecret
	for rows.Next() {
		var sec InstanceSecret
		if err := rows.Scan(&sec.Name, &sec.Value); err != nil {
			return nil, err
		}
		out = append(out, sec)
	}
	return out, rows.Err()
}

// ServiceInstance is one shared managed-service container the brain runs
// (SERVICE_PROVISIONING.md # Tier 1). Kind+Version identify it (e.g.
// postgres/15); SuperuserPassword is the credential the brain provisions per-app
// databases with via docker exec.
type ServiceInstance struct {
	Kind              string
	Version           string
	SuperuserPassword string
	State             string // running
	CreatedAt         time.Time
}

// GetServiceInstance returns the service instance of a given kind+version, or
// ErrNotFound when none has been spun up yet (the lazy-spinup trigger).
func (s *Store) GetServiceInstance(kind, version string) (ServiceInstance, error) {
	var si ServiceInstance
	var created int64
	err := s.db.QueryRow(
		`SELECT kind, version, superuser_password, state, created_at
		 FROM service_instances WHERE kind=? AND version=?`, kind, version,
	).Scan(&si.Kind, &si.Version, &si.SuperuserPassword, &si.State, &created)
	if errors.Is(err, sql.ErrNoRows) {
		return ServiceInstance{}, ErrNotFound
	}
	if err != nil {
		return ServiceInstance{}, fmt.Errorf("scan service_instance: %w", err)
	}
	si.CreatedAt = time.Unix(created, 0)
	return si, nil
}

// CreateServiceInstance records a freshly spun-up service instance. Caller
// generates the superuser password.
func (s *Store) CreateServiceInstance(si ServiceInstance) error {
	_, err := s.db.Exec(
		`INSERT INTO service_instances (kind, version, superuser_password, state, created_at)
		 VALUES (?,?,?,?,?)`,
		si.Kind, si.Version, si.SuperuserPassword, si.State, si.CreatedAt.Unix())
	if err != nil && isUniqueErr(err) {
		return ErrConflict
	}
	return err
}

// ListServiceInstances returns every recorded service instance, used by the
// startup reconcile pass to re-assert each is running.
func (s *Store) ListServiceInstances() ([]ServiceInstance, error) {
	rows, err := s.db.Query(
		`SELECT kind, version, superuser_password, state, created_at
		 FROM service_instances ORDER BY kind, version`)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	var out []ServiceInstance
	for rows.Next() {
		var si ServiceInstance
		var created int64
		if err := rows.Scan(&si.Kind, &si.Version, &si.SuperuserPassword, &si.State, &created); err != nil {
			return nil, err
		}
		si.CreatedAt = time.Unix(created, 0)
		out = append(out, si)
	}
	return out, rows.Err()
}

// ServiceGrant is one per-app database+role provisioned inside a service
// instance (SERVICE_PROVISIONING.md # Per-app isolation). LogicalName is the
// manifest's `services:` map key; the brain re-emits the credentials as
// MALMO_SERVICE_<LogicalName>_*.
type ServiceGrant struct {
	LogicalName string
	Kind        string
	Version     string
	DBName      string
	RoleName    string
	Password    string
}

// SetServiceGrants replaces an instance's service grants in one transaction.
// Called once per install after the databases+roles are provisioned. Mirrors
// SetInstanceSecrets.
func (s *Store) SetServiceGrants(instanceID string, grants []ServiceGrant) error {
	tx, err := s.db.Begin()
	if err != nil {
		return err
	}
	defer tx.Rollback()
	if _, err := tx.Exec(`DELETE FROM service_grants WHERE instance_id=?`, instanceID); err != nil {
		return err
	}
	for _, g := range grants {
		if _, err := tx.Exec(
			`INSERT INTO service_grants
			 (instance_id, logical_name, kind, version, db_name, role_name, password)
			 VALUES (?,?,?,?,?,?,?)`,
			instanceID, g.LogicalName, g.Kind, g.Version, g.DBName, g.RoleName, g.Password); err != nil {
			return err
		}
	}
	return tx.Commit()
}

// GetServiceGrants returns an instance's service grants, ordered by logical name.
