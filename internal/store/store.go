// CreateSession persists a freshly issued session. Caller picks the token.
func (s *Store) CreateSession(sess Session) error {
	_, err := s.db.Exec(
		`INSERT INTO sessions (token, user_id, created_at, last_seen_at, expires_at, elevated_until)
		 VALUES (?,?,?,?,?,?)`,
		sess.Token, sess.UserID, sess.CreatedAt.Unix(), sess.LastSeenAt.Unix(),
		sess.ExpiresAt.Unix(), sess.ElevatedUntil.Unix())
	return err
}

// GetSession looks up a session by its token. ErrNotFound on miss; the auth
// middleware maps that to 401.
func (s *Store) GetSession(token string) (Session, error) {
	var sess Session
	var created, lastSeen, expiresAt, elevatedUntil int64
	err := s.db.QueryRow(
		`SELECT token, user_id, created_at, last_seen_at, expires_at, elevated_until
		 FROM sessions WHERE token=?`, token,
	).Scan(&sess.Token, &sess.UserID, &created, &lastSeen, &expiresAt, &elevatedUntil)
	if errors.Is(err, sql.ErrNoRows) {
		return Session{}, ErrNotFound
	}
	if err != nil {
		return Session{}, fmt.Errorf("scan session: %w", err)
	}
	sess.CreatedAt = time.Unix(created, 0)
	sess.LastSeenAt = time.Unix(lastSeen, 0)
	sess.ExpiresAt = time.Unix(expiresAt, 0)
	sess.ElevatedUntil = time.Unix(elevatedUntil, 0)
	return sess, nil
}

// SetElevatedUntil marks a session elevated until the given time. Used by the
// POST /api/v1/auth/elevate handler (USERS_AND_GROUPS.md # Elevation in the UI).
func (s *Store) SetElevatedUntil(token string, until time.Time) error {
	_, err := s.db.Exec(
		`UPDATE sessions SET elevated_until=? WHERE token=?`, until.Unix(), token)
	return err
}

// TouchSession updates last_seen_at — called by the auth middleware on every
// authenticated request.
func (s *Store) TouchSession(token string, at time.Time) error {
	_, err := s.db.Exec(`UPDATE sessions SET last_seen_at=? WHERE token=?`, at.Unix(), token)
	return err
}

// DeleteSession revokes a single session (logout).
func (s *Store) DeleteSession(token string) error {
	_, err := s.db.Exec(`DELETE FROM sessions WHERE token=?`, token)
	return err
}

// DeleteSessionsForUser revokes every session belonging to a user. Used after
// password reset and on user deletion (AUTH.md # Recovery).
func (s *Store) DeleteSessionsForUser(userID string) error {
	_, err := s.db.Exec(`DELETE FROM sessions WHERE user_id=?`, userID)
	return err
}

// ListSessionsForUser returns all sessions for a user, ordered by created_at.
// Used in tests to inspect session state without knowing the token.
func (s *Store) ListSessionsForUser(userID string) ([]Session, error) {
	rows, err := s.db.Query(
		`SELECT token, user_id, created_at, last_seen_at, expires_at, elevated_until
		 FROM sessions WHERE user_id=? ORDER BY created_at`, userID)
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	var out []Session
	for rows.Next() {
		var sess Session
		var created, lastSeen, expiresAt, elevatedUntil int64
		if err := rows.Scan(&sess.Token, &sess.UserID, &created, &lastSeen, &expiresAt, &elevatedUntil); err != nil {
			return nil, err
		}
		sess.CreatedAt = time.Unix(created, 0)
		sess.LastSeenAt = time.Unix(lastSeen, 0)
		sess.ExpiresAt = time.Unix(expiresAt, 0)
		sess.ElevatedUntil = time.Unix(elevatedUntil, 0)
		out = append(out, sess)
	}
	return out, rows.Err()
}

func scanUser(row scanner) (User, error) {
	var u User
	var created int64
	err := row.Scan(&u.ID, &u.Username, &u.Role, &u.RecoveryHash, &created)
	if errors.Is(err, sql.ErrNoRows) {
		return User{}, ErrNotFound
	}
	if err != nil {
		return User{}, fmt.Errorf("scan user: %w", err)
	}
	u.CreatedAt = time.Unix(created, 0)
	return u, nil
}

// AuditEvent is one row in the append-only audit log (LOGGING.md # Schema sketch).
// actor_user_id is NULL for system events; actor_role is 'system' in that case.
type AuditEvent struct {
	ID          int64
	TS          int64  // unix epoch ms
	ActorUserID string // empty when NULL
	ActorRole   string // "admin" | "member" | "system"
	Action      string
	TargetKind  string // empty when NULL
	TargetID    string // empty when NULL
	SourceIP    string // empty when NULL
	Success     bool
	Metadata    string // JSON text, empty when NULL
}

// InsertAuditEvent appends one row. The table's triggers prevent UPDATE/DELETE.
func (s *Store) InsertAuditEvent(e AuditEvent) error {
	var actorUserID any
	if e.ActorUserID != "" {
		actorUserID = e.ActorUserID
	}
	var targetKind, targetID, sourceIP, metadata any
	if e.TargetKind != "" {
		targetKind = e.TargetKind
	}
	if e.TargetID != "" {
		targetID = e.TargetID
	}
	if e.SourceIP != "" {
		sourceIP = e.SourceIP
	}
	if e.Metadata != "" {
		metadata = e.Metadata
	}
	success := 0
	if e.Success {
		success = 1
	}
	_, err := s.db.Exec(
		`INSERT INTO audit_events
		 (ts, actor_user_id, actor_role, action, target_kind, target_id, source_ip, success, metadata)
		 VALUES (?,?,?,?,?,?,?,?,?)`,
		e.TS, actorUserID, e.ActorRole, e.Action,
		targetKind, targetID, sourceIP, success, metadata)
	return err
}

// AuditFilter controls which rows ListAuditEvents returns.
type AuditFilter struct {
	// ActorUserID, when set, restricts to rows where actor_user_id = this value
	// OR (target_kind = 'user' AND target_id = this value). Used for member
	// visibility. Empty means "no restriction" (admin view).
	ActorUserID string
	AfterID     int64 // cursor: return rows with id < AfterID (0 = from newest)
	Limit       int
}

// ListAuditEvents returns rows newest-first, respecting the visibility filter.
func (s *Store) ListAuditEvents(f AuditFilter) ([]AuditEvent, error) {
	limit := f.Limit
	if limit <= 0 {
		limit = 50
	}

	var rows *sql.Rows
	var err error
	if f.ActorUserID != "" {
		// Member view: own actions or events targeting self.
		if f.AfterID > 0 {
			rows, err = s.db.Query(
				`SELECT id, ts, actor_user_id, actor_role, action, target_kind, target_id,
				        source_ip, success, metadata
				 FROM audit_events
				 WHERE id < ?
				   AND (actor_user_id = ? OR (target_kind = 'user' AND target_id = ?))
				 ORDER BY id DESC LIMIT ?`,
				f.AfterID, f.ActorUserID, f.ActorUserID, limit)
		} else {
			rows, err = s.db.Query(
				`SELECT id, ts, actor_user_id, actor_role, action, target_kind, target_id,
				        source_ip, success, metadata
				 FROM audit_events
				 WHERE actor_user_id = ? OR (target_kind = 'user' AND target_id = ?)
				 ORDER BY id DESC LIMIT ?`,
				f.ActorUserID, f.ActorUserID, limit)
		}
	} else {
		// Admin view: all rows.
		if f.AfterID > 0 {
			rows, err = s.db.Query(
				`SELECT id, ts, actor_user_id, actor_role, action, target_kind, target_id,
				        source_ip, success, metadata
				 FROM audit_events WHERE id < ? ORDER BY id DESC LIMIT ?`,
				f.AfterID, limit)
		} else {
			rows, err = s.db.Query(
				`SELECT id, ts, actor_user_id, actor_role, action, target_kind, target_id,
				        source_ip, success, metadata
				 FROM audit_events ORDER BY id DESC LIMIT ?`,
				limit)
		}
	}
	if err != nil {
		return nil, err
	}
	defer rows.Close()
	var out []AuditEvent
	for rows.Next() {
		e, err := scanAuditEvent(rows)
		if err != nil {
			return nil, err
		}
		out = append(out, e)
	}
	return out, rows.Err()
}

func scanAuditEvent(row scanner) (AuditEvent, error) {
	var e AuditEvent
	var actorUserID, targetKind, targetID, sourceIP, metadata sql.NullString
	var success int
	err := row.Scan(
		&e.ID, &e.TS, &actorUserID, &e.ActorRole, &e.Action,
		&targetKind, &targetID, &sourceIP, &success, &metadata)
	if err != nil {
		return AuditEvent{}, fmt.Errorf("scan audit_event: %w", err)
	}
	e.ActorUserID = actorUserID.String
	e.TargetKind = targetKind.String
	e.TargetID = targetID.String
	e.SourceIP = sourceIP.String
	e.Metadata = metadata.String
	e.Success = success != 0
	return e, nil
}

// isUniqueErr matches modernc.org/sqlite's UNIQUE-constraint error without
// importing the driver's error type. modernc returns errors whose string
// contains "UNIQUE constraint failed".
func isUniqueErr(err error) bool {
	return err != nil && strings.Contains(err.Error(), "UNIQUE constraint failed")
}

type scanner interface{ Scan(dest ...any) error }

func scan(row scanner) (Instance, error) {
	var i Instance
	var created int64
	err := row.Scan(&i.ID, &i.ManifestID, &i.Name, &i.Slug, &i.Version, &i.State, &i.MDNSName, &created)
	if errors.Is(err, sql.ErrNoRows) {
		return Instance{}, ErrNotFound
	}
	if err != nil {
		return Instance{}, fmt.Errorf("scan instance: %w", err)
	}
	i.CreatedAt = time.Unix(created, 0)
	return i, nil
}
